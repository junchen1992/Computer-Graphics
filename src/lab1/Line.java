		Line line = new Line() {
			@Override
			void line(BufferedImage buf, int x0, int y0, int xn, int yn, int rgb) {
				int dx = xn - x0;
				int dy = yn - y0;

				// (1) slope = 0 (horizontal line):
				if (dy == 0) {
					for (int x = Math.min(x0, xn); x <= Math.max(x0, xn); x++) {
						buf.setRGB(x, y0, rgb);
					}
				}

				// (2) slope = infinity (vertical line):
				else if (dx == 0) {
					for (int y = Math.min(y0, yn); y <= Math.max(y0, yn); y++) {
						buf.setRGB(x0, y, rgb);
					}
				}

				// (3) 0 < slope <= 1:
				else if ((dy * dx > 0) && ((dy * 1.0) / dx <= 1)) {
					if (x0 > xn) {
						// swap(x0, xn);
						int xt = x0;
						x0 = xn;
						xn = xt;
						// swap(y0, yn);
						int yt = y0;
						y0 = yn;
						yn = yt;
						dx = -dx;
						dy = -dy;
					}
					int x = x0, y = y0;
					int D = (dy << 1) - dx;
					buf.setRGB(x, y, rgb);
					while (x < xn) {
						x++;
						if (D < 0) {
							D += (dy << 1);
						} else {
							y++;
							D += ((dy - dx) << 1);
						}
						buf.setRGB(x, y, rgb);
					}
				}

				// (4) -1 <= slope < 0:
				else if (((dy * 1.0) / dx >= -1) && (dy * dx < 0)) {
					if (x0 > xn) {
						// swap(x0, xn);
						int xt = x0;
						x0 = xn;
						xn = xt;
						// swap(y0, yn);
						int yt = y0;
						y0 = yn;
						yn = yt;
						dx = -dx;
						dy = -dy;
					}
					int x = x0, y = y0;
					int D = (dy << 1) + dx;
					buf.setRGB(x, y, rgb);
					while (x < xn) {
						x++;
						if (D < 0) {
							y--;
							D += (dx + dy) << 1;
						} else {
							D += (dy << 1);
						}
						buf.setRGB(x, y, rgb);
					}
				}

				// (5) 1 < slope < +infinity:
				else if ((dy * 1.0) / dx > 1) {
					if (x0 > xn) {
						// swap(x0, xn);
						int xt = x0;
						x0 = xn;
						xn = xt;
						// swap(y0, yn);
						int yt = y0;
						y0 = yn;
						yn = yt;
						dx = -dx;
						dy = -dy;
					}
					// System.out.println(x0 + ", " + y0 + "   ->   " + xn +
					// ", " + yn + " : " + (1.0 * dy / dx));
					int x = x0, y = y0;
					int D = dy - (dx << 1);
					buf.setRGB(x, y, rgb);
					while (y < yn) {
						y++;
						if (D > 0) {
							D -= (dx << 1);
						} else {
							x++;
							D += ((dy - dx) << 1);
						}
						buf.setRGB(x, y, rgb);
					}
				}

				// (6) -infinity < slope < -1:
				else {
					if (y0 > yn) {
						// swap(x0, xn);
						int xt = x0;
						x0 = xn;
						xn = xt;
						// swap(y0, yn);
						int yt = y0;
						y0 = yn;
						yn = yt;
						dx = -dx;
						dy = -dy;
					}
					int x = x0, y = y0;
					int D = -dy - (dx << 1);
					buf.setRGB(x, y, rgb);
					while (y < yn) {
						y++;
						if (D < 0) {
							D -= (dx << 1);
						} else {
							x--;
							D -= ((dx + dy) << 1);
						}
						buf.setRGB(x, y, rgb);
					}
				}

			}
		};

